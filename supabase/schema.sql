-- Trigger function to manage packing dates with rollback handling
CREATE OR REPLACE FUNCTION public.handle_packing_dates()
RETURNS TRIGGER AS $$
BEGIN
    -- Transition Not Started -> In Progress
    IF NEW.status = 'In Progress' AND OLD.status = 'Not Started' THEN
        IF NEW.packing_start_date IS NULL THEN
            NEW.packing_start_date := CURRENT_DATE;
        END IF;
    END IF;
    -- Transition In Progress -> Packed
    IF NEW.status = 'Packed' AND OLD.status = 'In Progress' THEN
        IF NEW.packing_end_date IS NULL THEN
            NEW.packing_end_date := CURRENT_DATE;
        END IF;
    END IF;
    -- Rollback In Progress -> Not Started
    IF NEW.status = 'Not Started' AND OLD.status = 'In Progress' THEN
        NEW.packing_start_date := NULL;
    END IF;
    -- Rollback Packed -> In Progress
    IF NEW.status = 'In Progress' AND OLD.status = 'Packed' THEN
        NEW.packing_end_date := NULL;
    END IF;
    -- Direct rollback Packed -> Not Started (clear both)
    IF NEW.status = 'Not Started' AND OLD.status = 'Packed' THEN
        NEW.packing_start_date := NULL;
        NEW.packing_end_date := NULL;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger on packing_items
CREATE TRIGGER trg_handle_packing_dates
BEFORE UPDATE OF status ON public.packing_items
FOR EACH ROW EXECUTE FUNCTION public.handle_packing_dates();

